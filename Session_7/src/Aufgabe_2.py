def addieren(value1, value2):
    return value1 + value2


def subtrahieren(value1, value2):
    return value1 - value2


def multiplizieren(value1, value2):
    return value1 * value2


def dividieren(value1, value2):
    return value1 / value2


print(addieren(10, 5))
print(subtrahieren(10, 5))
print(multiplizieren(10, 5))
print(dividieren(10, 5))
